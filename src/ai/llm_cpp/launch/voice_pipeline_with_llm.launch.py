from launch import LaunchDescription
from launch.actions import IncludeLaunchDescription
from launch.launch_description_sources import PythonLaunchDescriptionSource
from launch.substitutions import PathJoinSubstitution
from launch_ros.substitutions import FindPackageShare


def generate_launch_description():
    upstream = PathJoinSubstitution(
        [FindPackageShare("intent_router_cpp"), "launch", "voice_pipeline_with_router.launch.py"])
    llm = PathJoinSubstitution([FindPackageShare("llm_cpp"), "launch", "llm.launch.py"])

    return LaunchDescription([
        IncludeLaunchDescription(PythonLaunchDescriptionSource(upstream)),
        IncludeLaunchDescription(PythonLaunchDescriptionSource(llm)),
    ])
